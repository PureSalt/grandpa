'use strict';

import _ from 'lodash/fp';

const DEFAULT_KEYS = {
  LEFT: 37,
  RIGHT: 39,
  UP: 38,
  A: 65,
  D: 68,
  W: 87,
  SPACE: 32
};

export default class Controller {

  constructor(keys) {
    this.KEYS = _.clone(DEFAULT_KEYS);
    this.setKeys(keys);
  }

  setKeys(keys) {
    this.KEYS = _.defaults(keys || {}, this.KEYS);
  }

}
